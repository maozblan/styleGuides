<h1>splashes splashes</h1>

<p>
	splashes are the most unique out of all the types of art requested. they are
	resized and fitted to a certain area to fill up space.
</p>
<p>
	this may request the splash to be done in multiple parts with multiple SVGs to
	avoid stretching. due to the game being currently worked in a web based
	format, exact dimensions of the splash is hard to get, instead you will be
	given rounded ratios. please still keep splashes under 500px on both sides, it
	can be scaled up if necessary.
</p>
<p>
	it is also good to leave certain elements of the splash to be animated with
	simple CSS, such as the up and down motion or a fade in, fade out.
</p>

<br />

<p>
	the goal of a splash is to fill up space while being nonintrustive. the
	requirement for subtle motion is so the game doesn't look like it got stuck.
</p>
<p>
	while it is best to stick to solid blocks of color like characters and items,
	you may optionally choose to use path outlines. however, make sure they are
	CENTER and not inside or outside. remember not to overdo splashes as they are
	there to compliment and fill space, not exactly to catch and hold attention.
	they should also relate to or pertain to the page they are on.
</p>

<br />

<p>
	best example will be the <a
		href="https://jaxonruiz.github.io/GalacticCoffeeShop/"
		target="_blank">current home page</a
	> which is done in 3 layers and rotated in CSS with different speeds for parallax.
</p>
<p>
	you may request a splash to be semi cropped like the homepage to create more
	dynamic changes on the screen, though in-game splashes do not need to be so
	eye-catching.
</p>

